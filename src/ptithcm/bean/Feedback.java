package ptithcm.bean;

public class Feedback {
	private String comment;
	private byte score;
	
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public byte getScore() {
		return score;
	}
	public void setScore(byte score) {
		this.score = score;
	}
	public Feedback() {
		super();
	}
	
	
}
